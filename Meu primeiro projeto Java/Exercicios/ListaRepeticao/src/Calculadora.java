public class Calculadora {
    public static void soma(double n1, double n2) {
         double resultado = n1 + n2;    

 System.out.println( "O valor da soma é : " + resultado);
}
 public static void subtracao(double n1, double n2) {
         double resultado = n1 - n2;    

 System.out.println( "O valor da subtração é : " + resultado);
}
 public static void multiplicao(double n1, double n2) {
         double resultado = n1 * n2;    

 System.out.println( "O valor da multiplicacao é : " + resultado);
}
 public static void divisao(double n1, double n2) {
         double resultado = n1 / n2;    

 System.out.println( "O valor da divisao é : " + resultado);
}
}


